export function sum(a, b) {
    return a + b;
}
export function difference(a, b) {
    return a - b;
}
export function product(a, b) {
    return a / b;
}
export function quotient(a, b) {
    return a % b;
}
console.log("Suma: " + sum(10,5));
console.log("Difference: " + difference(10,5));
console.log("Product: " + product(10,5));
console.log("Quotient: " + quotient(10,5));